# Slice de textos ajudam para que a gente faça o fatiamento de um trecho de uma variável
var = "Eu sou um texto que vai sofrer slice!"

# Imprimindo uma posição específica
print(var[0])

# Imprimindo um intervalo
print(var[1:4]) # [posição inicial: posição final]

# Imprimindo a partir de uma posição até o final da string
print(var[3:])

# Imprimindo tudo até uma posição específica
print(var[:19])

# Verificando o tamanho da string(quantidade de caracteres, incluindo espaço)
print(len(var))